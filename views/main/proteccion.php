        <b><h2 class="h2proteccion" align= "center">Reglamento de uso de la página web para la adopción de mascotas</h2></b>
            <b><p class="pProteccion">Somos una comunidad de amantes por los animales, en nuestro afan de transmitir este sentimiento hemos
                    decidido crear esta aplicación.</p></b>
            </div>
            <div>
            <b><h3 class="h3proteccion"  align= "left">1. Objetivo de la página web</h3></b>
                <p class="pProteccion"> La página web tiene como objetivo principal facilitar la adopción de mascotas a personas interesadas en brindar un hogar responsable a un animal necesitado. </p>
            </div>
            <b><h3 class="h3proteccion" align= "left">2. Mascotas disponibles para adopción</h3></b>
                <p class="pProteccion"> En nuestra página web solo se publicarán mascotas que cumplan con los siguientes requisitos:
                    Mascotas rescatadas o abandonadas.
                    Mascotas que cuenten con un buen estado de salud.
                    Mascotas que hayan sido evaluadas por un veterinario.
                    Mascotas que hayan sido castradas o esterilizadas.
                </p>
            </div> 
            <div>
            <b><h3 class="h3proteccion" align= "left">3. Proceso de adopción</h3></b>
                <p class="pProteccion"> Para adoptar una mascota, los interesados deberán seguir los siguientes pasos:
                    Completar un formulario de solicitud de adopción en la página web.
                    Proporcionar información detallada acerca del hogar en el que se pretende adoptar a la mascota, incluyendo información sobre el lugar de residencia, el tipo de vivienda y el número de personas que convivirán con la mascota.
                    Proporcionar información sobre otras mascotas que convivan en el hogar.
                    Participar en una entrevista mediante correo electrónico para evaluar la idoneidad del candidato para adoptar una mascota.
                </p>
            </div> 
            <div>
            <b><h3 class="h3proteccion" align= "left">4. Responsabilidad de los adoptantes</h3></b>
                <p> class="pProteccion" Los adoptantes se comprometen a: <br>
                    Brindar una alimentación adecuada y un espacio seguro y cómodo para la mascota.<br>
                    Proporcionar atención veterinaria de manera regular y en caso de emergencia.<br>
                    Brindar amor y compañía a la mascota adoptada.<br>
                    Comprometerse a no usar la mascota con fines comerciales,<br> de experimentación o para cualquier otro fin distinto al de brindarle un hogar amoroso y responsable.
                </p>
            </div>
            <div>
            <b><h3  class="h3proteccion" align= "left">5. Condiciones para adoptar mascotas</h3></b>
                <p class="pProteccion"> Los usuarios interesados en adoptar una mascota deberán cumplir con los siguientes requisitos: <br>
                    El adoptante debe tener al menos 18 años. <br>
                    El adoptante debe tener una vivienda adecuada y segura para la mascota. <br>
                    El adoptante debe comprometerse a proporcionar cuidados adecuados, alimentación, ejercicio y atención médica a la mascota.</p>
            </div>
            <div>
            <b><h3 class="h3proteccion" align= "left">6. Responsabilidad de la página web</h3></b>
                <p class="pProteccion"> La página web se compromete a: <br>
                    Publicar únicamente mascotas que cumplan con los requisitos establecidos en el punto 2. <br>
                    Proporcionar información detallada acerca de la mascota en adopción. <br>
                    Brindar apoyo y seguimiento después de la adopción.</p>
            </div>
            <div>
            <b><h3  class="h3proteccion" align= "left">7. Prohibiciones</h3></b>
                <p class="pProteccion"> No está permitido:  <br>
                    Publicar información falsa o engañosa. <br>
                    Publicar contenido ofensivo, discriminatorio o difamatorio. <br>
                    Publicar material protegido por derechos de autor sin el permiso del propietario. <br>
                    Realizar actividades ilegales a través del sitio web.</p>
            </div> 
            <div>
            <b><h3  class="h3proteccion" align= "left">8. Condiciones para dar en adopcion de mascotas</h3></b>
                <p class="pProteccion"> Solo se permitirá dar en adopción a mascotas que cumplan con los siguientes requisitos: <br>
                    La mascota debe estar en buen estado de salud y haber recibido todas las vacunas necesarias. <br>
                    La mascota no debe tener comportamientos agresivos hacia las personas u otros animales. <br>
                    El propietario de la mascota debe proporcionar información detallada sobre la personalidad y las necesidades de la mascota, <br> así como cualquier otra información relevante para su adopción.
                    
                </p>
            </div> 
